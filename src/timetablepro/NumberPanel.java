    static Color BORDER = convertColour(javafx.scene.paint.Color.CHOCOLATE);
    static Color BACKGROUND = convertColour(javafx.scene.paint.Color.BISQUE);
    static int WIDTH = 100, HEIGHT = 50, ARC_AMOUNT = 15;
    static Font FONT = new Font("Segoe UI Semibold", Font.PLAIN, 36);
    public int num = 10;
