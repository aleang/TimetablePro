    @Override
    public Dimension preferredSize() {
        return new Dimension(WIDTH+5, HEIGHT+5);
    }
    public static Color convertColour(javafx.scene.paint.Color c) {
        int[] col = new int[3];
        col[0] = (int)(c.getRed() * 255);
        col[1] = (int)(c.getGreen()*255);
        col[2] = (int)(c.getBlue() *255);
        return new java.awt.Color(col[0], col[1], col[2]); 
    }

    public int get() {
        return num;
    }
    @Override
    public void paintComponent(Graphics g) {
        g.setColor(BACKGROUND);
        g.fillRoundRect(0, 0, WIDTH, HEIGHT, ARC_AMOUNT, ARC_AMOUNT);
        
        g.setColor(BORDER);
        g.drawRoundRect(0, 0, WIDTH, HEIGHT, ARC_AMOUNT, ARC_AMOUNT);
        
        // draw number
        
        if (num == 0) return;
        String text = String.valueOf(num);
        g.setFont(FONT);
        FontMetrics fm = g.getFontMetrics();
        int totalWidth = fm.stringWidth(text);
        
        // absolutely centred
        int x = (WIDTH / 2) - (totalWidth / 2);
        int y = (HEIGHT - fm.getHeight()) / 2 + fm.getAscent(); 
        //x -= fm.stringWidth(text) / 2;
        
        g.setColor(BORDER);
        g.drawString(text, x, y);
    }
